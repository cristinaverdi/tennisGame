
public class PlayerPerformance {

    private int serve;
    private int skill;
    private int stamina;


    public PlayerPerformance(int serve, int skill, int stamina) {
        this.serve = serve;
        this.skill = skill;
        this.stamina = stamina;
    }

    public int getServe() {
        return serve;
    }

    public int getSkill() {
        return skill;
    }

    public int getStamina() {
        return stamina;
    }
}


